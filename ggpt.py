def get_braille_char(dots):
    # dots is list of 6 bools [top-left, top-mid, top-right, bot-left, bot-mid, bot-right]
    idx = 0
    if dots[0]: idx += 1
    if dots[1]: idx += 2
    if dots[2]: idx += 4
    if dots[3]: idx += 8
    if dots[4]: idx += 16
    if dots[5]: idx += 32
    return BRAILLE[idx]

